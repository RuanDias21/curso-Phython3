"""
Pythhon = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

print(1234)  # Aqui foi impresso um número do tipo int, ou inteiro.

# Aspas simples
print('123')  # Aqui fo impresso uma cadeia de caracteres de números, ou seja, tipo str ou texto.
print('Lucas Manesco')
print('Lucas "Manesco"') #pode usar assim invés de colocar o r 

# Aspas duplas
print("Lucas Manesco")
print("Lucas 'Manesco'")

# Escape
print("As horas \"voaram\"")  # Caractere de escape é a barra invertida "\".

# r
print(r"David \"Rodrigues\"")  # É possível imprimir caracteres especiais dessa forma.


